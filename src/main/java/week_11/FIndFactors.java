package week_11;

public class FIndFactors {
    public static void main(String[] args) {
        int n = 0;
        int factors = 0;
        for (int i = 1; i <= n; i++) {
            if (n % 1 == 0) {
                factors++;
                System.out.println("n is dividable by " + i);
            }
        }
    }
}
